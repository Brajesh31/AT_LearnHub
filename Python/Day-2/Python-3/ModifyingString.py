
#Modifying String
a = "My Name is Akshay" 
print(a.upper())  

#Lower Case
a = "My Name is Akshay" 
print(a.lower())  

 #Removing White Spaces
a = " My Name is Akshay " 
print(a.strip()) 

#Replace String
a = "My Name is Akshay" 
print(a.replace("a", "J"))  

#Split String
a = "My Name is Akshay" 
print(a.split(" "))
